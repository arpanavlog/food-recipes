    <font size="7"><u><b>7) Rasmalai:</b></u></font>
<center><img src="../images/rasmalai.jpg"width="30%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Rasmalai is an easy and delicious Indian sweet recipe, which is loved by almost 
everyone! This scrumptious dessert is quite popular in Indian households 
and is made during various festivals and special occasions. <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>1/2 litre milk</li><br>
<li>6 tablespoon sugar</li><br>
<li>1/2 teaspoon sliced almonds</li><br>
<li>1/4 teaspoon powdered black cardamom</li><br>
<li>1/2 cup water</li><br>
<li>50 gm paneer</li><br>
<li>1/2 pinch saffron</li><br>
<li>1/2 teaspoon sliced pistachios</li><br>
<li>1 teaspoon all purpose flour</li><br></ul>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Rasmalai recipe is a simple and delectable recipe. To make this recipe 
take a bowl and add crumbled paneer in it. Mix it with all purpose flour 
and knead to a smooth dough. Make small balls from the dough and flatten 
them lightly using your palms.</li><br>

<li>Now take a pan and add 1/2 cup water in it. Heat it over high flame. 
Add 3 tbsp sugar and bring the mixture to a boil until the sugar has fully 
dissolved. Once it's done, remove from the flame. While the mixture is still
 hot, add the paneer balls and cover it with a lid. Keep the pan aside for 
 few hours.</li><br>
 

<li>Now take another heavy pan and add milk in it. Boil it until reduced to 
half. Then add the remaining sugar and stir well until fully dissolved.
 Remove the mixture from the flame. Add cardamom powder, saffron strands 
 and mix well.</li><br>
  

<li>Remove the paneer balls from the sugar syrup and squeeze them gently. 
Add them to the hot milk and garnish with almonds and pistachios. Keep the 
Rasmalai in the fridge for 2 to 3 hours as it is best enjoyed when chilled.</li><br></ul>

