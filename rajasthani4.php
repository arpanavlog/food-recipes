     <font size="7"><u><b>4) Dil khushal-</b></u></font>
<center><img src="../images/dil khushal.jpg"width="50%"/></center><br>
<font size="6"><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Rajasthan is known for its toothsome sweets. This  mithai  is also popular as  
Mohanthaal  or  Besan ki Barfi.  It has a nice chewy texture and leaves a grainy 
flavour in your mouth.<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">

<li>coarsely ground besan (Bengal gram flour,2 cups)</li><br>
<li>ghee (1 cup )</li><br>
<li>grated mava (khoya,1 cup/150 grams)</li><br>
<li>cardamom powder (elaichi,1 tsp )</li></ul><br>

<font color="black" size="7"><b>For The Sugar Syrup:</b></font><br><br>
<ul type="disc">

<li>sugar, 1 1/2 cups (300 grams)</li><br>
<li>water 1 cup</li><br>
<li>milk 2 tbsp</li></ul><br>

<font color="black" size="7"><b>For Garnish:</b></font><br><br>
<ul type="disc">
<li>Chopped Almond and Pistas</li></ul><br>

<font color="#bf55ec" size="7"><u>Dil khushal Process:</u></font><br><br>
<ul type="disc">
<li>Take the gram flour in a bowl.</li><br>
<li>In a pan Heat the ghee and add half of it to the gram flour.</li><br>
<li>Mix the ghee into the gram flour till the mixture become like bread crumbs.</li><br>
<li>Put the remaining hot ghee in a pan, put the gram flour mixture and cook till 
the mixture turns golden brown, stir continuously.</li><br>
<li>Now add the grated khoya and cardamom powder and stir for about 10 minutes.</li><br>
<li>Take this away from the heat and keep aside to cool.</li></ul><br>


<b>For the sugar syrup:</b><br>
<ul type="disc">
<li>Add enough sugar with water in a pan and simmer for 10 minutes, let it boil.</li><br>
<li>Add 2 tablespoon of milk to the boiling sugar syrup.</li><br>
<li>Now the impurities will from a gray layer. Take out this layer gently using a
 slotted spoon.</li><br>
<li>Keep it on boil till the syrup is of 1 string consistency and keep it hot.</li></ul><br>


<b>How to proceed:</b><br>
<ul type="disc">
<li>Now add the hot sugar syrup over the cooked gram flour mixture and mix well.</li><br>
<li>Now pour into a greased surfaced thali.</li><br>
<li>Add some chopped almonds and Pistas on top and allow to set for at least 5 hours.</li><br>
<li>Cut into pieces of your choice.</li><br>
<li>Serve and Enjoy.</li></ul>

