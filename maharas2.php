    <font size="7"><u><b>2) Aamras:</b></u></font>
<center><img src="../images/aamras.jpg"width="35%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Traditionally only Haapus or Alphonso mangoes are used to make this dish, 
but you can also substitute it with Banganphali or any variety that has 
smooth non-fibrous flesh. <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Ripe mangoes, preferably alphonso (6-8)</li><br>
<li>Sugar, optional (4 tbsp)</li><br>
<li>Cardamom seeds (elaichi), crushed (optional) (1/2 tsp)</li><br>
<li>Saffron (kesar) (1/8 tsp)</li><br>
<li>Salt (A pinch )</li></ul><br>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Wash and peel the mangoes.</li><br>

<li>Squeeze out as much pulp as possible, discard the seeds.</li><br>

<li>Place the mango pulp, sugar, elaichi and salt in a blender and grind until 
smooth and creamy.</li><br>

<li>If the mangoes are naturally sweet then you need not add extra sugar.</li><br>

<li>Add saffron to the prepared pulp and refrigerate to cool.</li><br>

<li>Aamras is best enjoyed with puris or as a dessert, garnished with dry fruits.</li></ul><br>
  
