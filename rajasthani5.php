     <font size="7"><u><b>5) Gatte ki khichdi:</b></u></font>
<center><img src="../images/gatte ki khichdi.jpg"width="50%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Gatte ki Khichdi  is undoubtedly the best rice preparation one can have in Rajasthan. 
This flavourful dish prepared with rice and soft  besan  dumplings is the perfect evening 
snack.<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>

<font size="7" color="black"><b>For the gattas:</b></font><br><br>
<ul type="disc">

<li>Chickpea Flour / Besan : 1 cup</li><br>
<li>Fennel seeds / saunf : 1 tsp</li><br>
<li>Carom seeds / ajwain : 1/2 tsp</li><br>
<li>Red chilli powder : 1 tsp (or as per taste)</li><br>
<li>Salt as per taste</li><br>
<li>Yogurt : 1 tbsp</li><br>
<li>Oil : 2 tbsp + more for deep frying the gattas</li></ul><br>

<font size="7" color="black"><b>For the Rice:</b></font><br><br>
<ul type="disc">

<li>Basmati rice (uncooked) : 1 cup</li><br>
<li>Onion (thinly sliced) :3/4 cup</li><br>
<li>Cardamom / Elaichi : 1</li><br>
<li>Cloves / Lavang : 3</li><br>
<li>Cumin seeds / jeera : 1/2 tsp</li><br>
<li>Mustard seeds / rai : 1/2 tsp</li><br>
<li>Red chilli powder : 1 tsp (or as per taste)</li><br>
<li>Asafoetida / Hing : 1/4 tsp</li><br>
<li>Turmeric powder : 1/2 tsp</li><br>
<li>Garam masala : 1/2 tsp</li><br>
<li>Salt as per taste</li><br>
<li>Ghee / Clarified butter : 2 tbsp (oil can be used instead)</li><br>
<li>Cilantro (chopped) : 2 tbsp</li><br>
<li>Oil  for frying the onions.</li></ul><br>

<font size="7" color="black"><b>To be ground into paste:</b></font><br><br>
<ul type="disc">

<li>Onion (sliced) : 1/2 cup</li><br>
<li>Green chillies : 2</li><br>
<li>Ginger : 1.5 inch</li><br>
<li>Garlic : 4 cloves</li></ul><br>

<font size="7" color="#bf55ec"><u>Procedure:</u></font></u><br><br>
<font size="7" color="black"><b>Preparing the gattas:</b></font><br><br>
<ul type="1">

<li>Mix the chickpea flour, fennel seeds, carom seeds, chilli powder, salt, yogurt and 2 tbsp. 
Bind the ingredients using little water into a stiff dough.</li><br>

<li>Divide the dough into 8 portions. Shape each of these portions into cylindrical dumplings 
just like in the picture below.</li><br>

<li>Boil enough water in a big pot. Once it starts to boil, add the cylindrical dumplings and 
cook them for 12-15 minutes.</li><br>

<li>Drain the water and set the gattas aside to cool.</li><br>

<li>Once it has cooled, cut them into small pieces and deep fry them in hot oil.Drain them on 
an absorbent paper and set it aside.</li></ul><br>

<font size="7" color="black"><b>Preparing the paste:</b></font><br><br>
<ul type="disc">

<li>Grind all the ingredients under the "to be ground into paste" section into a smooth paste. 
Set it aside.</li></ul><br>


<font size="7" color="black"><b>Preparing the rise:</b></font><br><br>
<ul type="1">

<li>Boil a lot of water in a deep pot. When the water comes to a boil, add the washed rice. 
Once the rice is added, bring the water back to boil and then reduce the heat to low-med and 
continue to cook the rice until  cooked (just the way we cook rice for biryanis).Drain the rice 
and set it aside until ready to use.</li><br>

<li>Heat oil for deep frying.Once the oil is hot, deep fry the onions.Set it aside.</li><br>

<li>In a deep bottomed pan / vessel , heat ghee. Once hot, add the cardamom, cloves, cumin seeds, 
mustard seeds and asafoetida.</li><br>

<li>When the seeds start to splutter, add the ground paste and saute for 5-7 minutes.</li><br>

<li>Add the turmeric powder, red chilli powder and garam masala and mix well.</li><br>

<li>Add the rice, fried onions and the fried gattas. Add salt and mix well.</li><br>

<li>Lastly add chopped cilantro and serve hot with kadhi (KHATTA) or chilled yogurt.</li></ul>

