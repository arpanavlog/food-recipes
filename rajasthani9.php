    <font size="7"><u><b>9) Mewari Paneer:</b></u></font>
<center><img src="../images/mewari paneer.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
If you want to surprise your guests with your culinary skills and treat them, 
try this Mewari Paneer recipe. Prepared with homemade paneer, 
this side dish recipe has a rich taste.<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>1 litre full cream milk</li><br>
<li>2 gm chopped raisins</li><br>
<li>2 gm chopped pistachios</li><br>
<li>1 pinch saffron</li><br>
<li>3 gm red peppers</li><br>
<li>125 gm chopped onion</li><br>
<li>5 gm sliced onion pearl</li><br>
<li>10 gm garlic paste</li><br>
<li>25 ml refined oil</li><br>
<li>10 gm cumin powder</li><br>
<li>8 gm fresh cream</li><br>
<li>5 gm white butter</li><br>
<li>10 gm chopped coriander leaves</li><br>
<li>2 gm chopped almonds</li><br>
<li>2 gm chopped walnuts</li><br>
<li>2 gm chopped cashews</li><br>
<li>4 ml lemon juice</li><br>
<li>3 gm capsicum ( green pepper)</li><br>
<li>150 gm tomato</li><br>
<li>10 gm ginger paste</li><br>
<li>25 ml tomato puree</li><br>
<li>10 gm coriander powder</li><br>
<li>6 gm garam masala powder</li><br>
<li>2 pinches salt</li><br>
<li>10 gm powdered deggi chilli</li><br>
<li>3 gm black cumin seeds</li><br></ul>


<font size="7" color="#bf55ec"><u>How to make Mewari Paneer:</u></font></u><br><br>
<ul type="disc">

<li>To prepare the homemade paneer, boil milk in a handi and curdle 
with lemon juice..</li><br>
 
<li>When the milk is curdled, add chopped dry fruits and saffron in it. Strain 
the mixture in muslin cloth and let it stand at least for an hour..</li><br>
 
<li>When the paneer is prepared, dice the paneer pieces.<br>
 
<li>Now, heat oil in a saucepan over medium flame and put all the 
ground spices in it. When the seeds starts to crackle, put black 
cumin add chopped onion in it..</li><br>

<li>When it's brown, add ginger and garlic paste, chopped tomatoes, 
sliced white onion (white pickled onion).</li><br>

<li>Then add all the dry spices and stir it when leaves the oil on the top, 
add diced paneer and bell pepper in it.</li><br>

<li>Garnish with fresh cream and chopped green coriander.</li><br>

