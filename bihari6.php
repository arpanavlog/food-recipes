        <font size="7"><u><b>6) Kala-Jamun-</b></u></font>
            <center><img src="../images/Kala-Jamun.jpg" width="40%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Kala Jamun is a delicious Indian dessert very similar to Gulab Jamun. 
            It is an exotic sweet dish made with paneer, khoya and saffron. 
            It has a soft saffron filled interior and is tender from inside and hard from the outside. 
            Kala Jamun is a popular sweet dish in Bihar and other parts of Eastern India where it is readily available in sweet shops.
            But it can also be prepared at home with perfection by following this quick and simple recipe. Try out!<br>
 
            <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

            <ul type="#">

              <li> Hariyali Khoya - 3/4 cup</li><br>
              <li> Cottage Cheese - 1/2 cup</li><br>
              <li> Refined flour (maida) - 3 1/2 tablespoons</li><br>
              <li> Cornflour/ corn starch - 3 1/2 tablespoons</li><br>
              <li> Sugar - 1 1/2 cups</li><br>
              <li> Milk - 1 tablespoon</li><br>
              <li> Green cardamom powder - 1/4 teaspoon</li><br>
              <li> Ghee to deep fry</li><br>
              <li> Silver warq as required.</li></ul><br>

     <font size="7" color="#bf55ec"><u>Instructions</u></font><br><br>
        <ul type="#">
                
          <li> Grate the khoya and cottage cheese separately and mix.
          Add the refined flour and cornflour, and knead well till smooth. Divide into twenty-five equal portions.</li><br>
   
          <li> Take each portion in your palm, press and roll into a smooth ball. Ensure there are no cracks on the surface.</li><br>

          <li> Bring the sugar and one and one-fourth cups of water to a boil, stirring till the sugar dissolves.
          Add the milk. Collect the scum which rises to the surface with a ladle, and discard.</li><br>
  
          <li> Add the cardamom powder and simmer till the syrup turns a light golden. Keep the syrup warm.</li><br>

          <li> Heat sufficient ghee in a non-stick kadai on medium heat. Gently slide in half the khoya balls.
          Lift the kadai off the heat and place on the worktop rotate it gently till the balls float to the surface.</li><br>
   
          <li> Place the kadai back on the heat and continue to fry on medium heat, stirring gently, till they turn dark brown in colour.</li><br>

          <li> Drain and dip them in the syrup. Let them soak for at least fifteen minutes before serving. Decorate with silver foil.</li><br>

          <li> Serve warm or cold.</li></ul><br>
  
