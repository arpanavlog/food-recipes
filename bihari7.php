        <font size="7"><u><b>7) Balushahi-</b></u></font>
            <center><img src="../images/Balushahi.jpg" width="40%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Balushahi sweet most famous in Sitamarhi we can get the perfect essence of this sweet in Runni Saidpur a village on way to Sitamarhi. 
            This is the place where you can get a perfect balushahi..<br><br>

        <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

        <ul type="#">
   
          <li>-> Maida - 4cup</li><br>
          <li>-> Baking Soda - 1tsp</li><br>
          <li>-> Ghee - 1 1/2 cup</li><br>
          <li>-> Curd - 1 cup</li><br>
          <li>-> Cardamoms - 5gm</li><br>
          <li>-> Pistachio nuts - 10gm</li><br>
          <li>-> Refined Oil - for frying</li></ul><br>


        <font size="7" color="black"><b> For Syrup-</b></font><br><br>
          <ul type="disc">
            <li>-> Sugar - 6 cups</li><br>
            <li>-> Lime Juice - 1tsp</li><br>
            <li>-> Rose essence - few drops</li></ul><br>

       <font size="7" color="#bf55ec"><u>Instructions</u></font><br><br>
          <ul type="#">
          
            <li> Add crushed cardamoms Mix curd & ghee and rub it. Add cold water to make a soft dough. Divide into equal sized balls.</li><br>

            <li> Flatten each ball between the palms sa as to have the centre thinner than the sides. Put into deep hot fat on slow fire, leave them 
            in without disturbance for 10 minutes. Turn over and repeat the process.</li><br>

            <li> When cooked, remove from fat and drain. To prepare syrup boil water and sugar and stir till dissolved.</li><br> 

            <li> Cook to one string consistency, sprinkle lime juice and rose essence.</li><br>

            <li> When balushahi is quite cold dip them in prepared sugar syrup and garnish with pistachio nuts.</li></ul><br>

