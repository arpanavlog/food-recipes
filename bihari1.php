      <font size="6"><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;          
	  Litti Chokha is a traditional dish eaten mainly in Bihar and Jharkhad.
         <br> 
            It can be had for lunch, dinner or you can make it on your holidays and 
            you will surely love it.<br> Litti is similar to Bati in appearance but there 
            is a little difference.<br> The Pitthi(stuffing) to be filled in it is made with 
            Sattu and Litti is eaten with brinjal bharta or mashed potato. <br>We can also 
            prepare Mixed Veg Chokha. <br>So let us start the preparation for Litti Choka.<br><br><br>

            <font size="7" color="#bf55ec"><u>Ingredients For Litti:</u></font><br><br>   
            <font color="black" size="7"><b>For dough:</b></font><br><br>
               <ul type="disc">
                  <li> Wheat flour - 400 grams (2 cup)</li><br>
                  <li> Ajwain(carom seeds) - 1/2 tsp</li><br>
                  <li> Ghee - 2 tbsp</li><br>
                  <li> Curd - 3/4 cup</li><br>
                  <li> Baking soda - 1/2 tsp</li><br>
                  <li> Salt - 3/4 tsp</li><br></ul>
            <font color="black" size="7"><b>For stuffing:</b></font><br><br>
               <ul type="disc">
                  <li> Sattu - 200 grams (1 cup)</li><br>
                  <li> Ginger - 1 inch long piece</li><br>
                  <li> Green chilly - 2 to 4</li><br>
                  <li> Green coriander(dhaniya) - 1/2 cup(finely chopped)</li><br>
                  <li> Jeera(cumin seeds) - 1 tsp</li><br>
                  <li> Ajwain(carom seeds) - 1 tsp</li><br>
                  <li> Mustard oil - 1tsp</li><br>
                  <li> Pickle spices - 1 tbsp</li><br>
                  <li> Lemon - juice of 1 lemon (if you prefer)</li><br>
                  <li> Salt - add to taste ( 1/2 tsp)</li></ul><br>
	
            <font size="7" color="#bf55ec"><u>Ingredients For Chokha:</u></font></u><br><br>	
               <ul type="disc">

                  <li> Brinjal(big) - 400 grams (1 or 2)</li><br>
                  <li> Tomatoes - 250 grams ( 4 medium sized)</li><br>
                  <li> Green chillies - 2 to 4 (finely chopped)</li><br>
                  <li> Ginger - 1 1/2 inch long piece ( finely chopped)</li><br>
                  <li> Green coriander - 2 tbsp ( finely chopped)</li><br>
                  <li> Salt - add to taste (1 tsp)</li><br>
                  <li> Mustard oil - 1 to 2 tsp</li></ul>

      <p><font color="#bf55ec" size="7"><u>How to make Litti Chokha:</u></font><br><br>	

               <font color="black" size="7"><b>Prepare dough for Litti:</b></font><br><br> 
               Filter the flour and keep it in a utensil, put Ghee,
               baking soda, Ajwain and salt in flour then mix well.<br><br>
               Beat curd and put it in flour as well.<br>
               knead a soft dough using warm water.<br><br>
               Cover the dough and keep aside for 30 minutes. Dough required to make Litti is ready.<br><br>

               <font color="black" size="7"><b>Make Stuffing:</b></font><br><br> 
               Now grind rosted gram to powder along black pepper (if sattu is not available ).<br><br>
               Take out mixture in a mixing bowl, add in all the spices and grated ginger, green chillies,
               coriander, lemon juice, and mix well.<br><br> If it seems too dry, add a tsp of oil and few drops of water to it.<br><br>
               The texture should be crumbly, Keep aside.<br><br>
 
           <font color="black" size="7"><b>Make Litti:</b></font><br><br> 
               Simply roll it like a small chapati, but dont apply any extra flour to it.<br><br>
               Place 2 tsp filling on it, pinch it from all sides so that filling gets cover from all sides as shown in picture below.<br><br>
               Pinch the extra dough from top,shape it round like a ball.<br><br>
               Litti is ready for baking. Now make 10-12 balls. Preheat the oven at 200 degrees.<br><br> Place all the balls on a baking dish and bake
               them for 30-40 minutes. Flip the side half way through, you may have to do it 2-3 times in between to get the even cooking done.<br><br>
 
            <font color="black" size="7"><b>Make Chokha:</b></font><br><br>  
               Boil the potatoes remove the skin and keep aside.<br><br>
               Roast brinjal and tomatoes in the oven or on the gas stove on medium flame from both the sides untill
               it becomes soft. Peel of the burnt skin of the brinjal.<br><br>
               Mash boiled potatoes, brinjal along with garlic and tomatoes together.<br><br>
               chokaAdd finely chopped onion, coriander leaves, grated ginger salt, green chillies and mustard oil.<br><br>
               chokaAdd finely chopped onion, coriander leaves, grated ginger salt, green chillies and mustard oil.<br><br>
               Mix all the ingredients with hands properly. Chokha is ready..<br><br>

               <font color="#bf55ec" size="7"><u>Assemble and serve:</u></font><br><br>	
 
               Put Chokha in a bowl, dip hot Litti in melted Ghee and serve with green coriander chutney.<br><br>
               Litti can also be broken through the centre and then ghee can be poured on top, this way it reaches the stuffing too.<br>
