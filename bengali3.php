    <font size="7"><u><b>3) Pushpanna:</b></u></font>
<center><img src="../images/Pushpanna.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Traditionally pushpanna's rice is prepared with Gobindo Bhog rice which gives the
 rice that aroma of sweet buttery flavour...<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Gobindo bhog rice, cooked (4 cups)</li><br>
<li>Green peas (matar) (1/2 cup)</li><br>
<li>Black pepper powder (kali mirch) (1 tsp)</li><br>
<li>Cloves (laung) (1/2 tsp)</li><br>
<li>Cardamom powder (elaichi) (1/2 tsp)</li><br>
<li>Fennel seeds (saunf) (1/2 tsp)</li><br>
<li>Cumin seeds (jeera) (1/2 tsp)</li><br>
<li>Cinnamon (dalchini) (1" piece)</li><br>
<li>Asafoetida (hing) (1/4 tsp)</li><br>
<li>MTR Lal Mirch/Red Chilli Powder (1/2 tsp)</li><br>
<li>Paneer cubes (1 cup)</li><br>
<li>Shredded coconut (nariyal) (1 tbsp)</li><br>
<li>Saffron (zafran/kesar) (1/2 tsp)</li><br>
<li>Milk (1/4 cup)</li><br>
<li>Salt ( to taste)</li><br>
<li>Oil (1 cup)</li><br>
<li>Clarified butter (ghee) (2 tbsp)</li><br></ul>

<font size="7" color="black"><b>For Tadka:</b></font></u><br><br>
<ul type="disc">
<li>Clarified butter (ghee) (2 tbsp)</li><br>
<li>Raisins (kismis) (2 tbsp)</li><br>
<li>Cashew nuts (kaju), chopped (1 tbsp)</li><br>
<li>Almonds (badam), sliced (1 tbsp)</li></ul><br>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Warm the milk and add saffron to create an infusion. Pour this milk over
 the cooked rice and gently stir to colour the rice. Set aside.</li><br>
 
<li>Boil the green peas in one cup of salted water for 3â€“4 mins, drain the water 
and allow them to cool.</li><br>

<li>Deep fry the paneer cubes in oil until golden brown, place on an absorbent
 paper to remove excess oil.</li><br>
 
<li>Heat ghee in a large kadhai, add fennel seeds, cumin seeds, cinnamon stick, 
cloves, cardamom powder, black pepper, asafoetida and MTR Lal Mirch/Red Chilli
 Powder. Cook for about a minute.</li><br>
 
<li>Next, add the cooked peas, paneer cubes, coconut and salt; cook further for 
3-5 mins.</li><br>

<li>Finally add the saffron rice and cook on a low flame for 5 mins.</li><br>

<li>Heat ghee in a pan and lightly saute the almonds, cashews and raisins and 
sprinkle them on top of the cooked rice.</li><br>

<li>Serve the deliciously rich pushpanna with your favourite raita and cholar dal.<br></li></ul><br>

