      <font size="7"><u><b>3) Laal maas-</b></u></font>
<center><img src="../images/laal maas.jpg"width="50%"/></center><br>
<font size="6"><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
You cannot talk about Rajasthani dishes without mentioning the crowd favourite,  Laal Maas  . This spicy dish gets its colour from the fiery red chillies. 
Best served with  bajra rotis  , this dish with plain rice won't disappoint you. <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">

<li>4 tbsp Mustard oil</li><br>
<li>3-4 Clove</li><br>
<li>6-8 Black peppers </li><br>
<li>3-4 Black cardamom</li><br>
<li>3-4 Green cardamom</li><br>
<li>1 inch Cinnamon </li><br>
<li>2 Tej patta </li><br>
<li>2 cups Onion  thinly sliced</li><br>
<li>2 tsp Fresh ginger garlic paste</li><br>
<li>3 tsp Kashmiri red chilli powder</li><br>
<li>1/2 tsp Turmeric powder</li><br>
<li>2 tsp Coriander </li><br>
<li>1/2 tsp Roasted cumin powder</li><br>
<li>Salt to taste</li><br>
<li>750 g Goat mutton</li><br>
<li>1 tbsp Lemon juice</li><br>
<li>Fresh coriander for garnishing</li></ul><br>


<font size="7" color="#bf55ec"><u>Instructions:</u></font></u><br><br>
<ul type="1">


<li>Heat oil in a heavy bottom Karahi / pan.</li><br>


<li>When the oil is very hot and fuming, switch off the heat.</li><br>


<li>Let it cool for 2 minutes.</li><br>


<li>Switch on the heat again.</li><br>


<li>Add cloves, black pepper, black cardamom, green cardamom, cinnamon and tejpatta.</li><br>


<li>Add the onions and fry till slightly brown.</li><br>



<li>Now add ginger garlic paste and fry for 3-4 minutes, until onion are nicely browned.</li><br>



<li>Add the mutton and fry for 3-4 minutes on high heat.</li><br>



<li>Add curd and fry for 3-4 minutes.</li><br>



<li>Now add Kashmiri red chilli powder, turmeric powder, coriander powder, roasted cumin powder
 and salt.</li><br>


<li>Add little water if required and fry till oil starts to separate.</li><br>


<li>Add a cup of water and cover the pan and cook the mutton till it is soft and tender.
 ( You can also use a pressure cooker to cook the mutton )</li><br>



<li>Next up add lemon juice.</li><br>


<li>Garnish with fresh coriander.</li><br>


<li>Serve hot with naan or rice...</li></ul>

