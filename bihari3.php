        <font size="7"><u><b>3) Dal-Puri-</b></u></font>
            <center><img src="../images/Dal-puri.jpg" width="40%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Bihari Channa Dal Puri Recipe is a traditional Bihari recipe.<br>
            No salt is added to the dish during festivals and is served with kheer after offering dal puri and kheer to Gods.
            To make these Dal puris, one can also add moong dal instead of channa dal which is used in this recipe. It is again an option when it comes to mashing the dal though it can be mashed fully to get a creamy texture.<br> 
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I have not mashedit completely to preserve the nutty dal flavour in every bite. Bihari Channa Dal Puri can be served with any Indian 
            curry of your choice or with kheer like Bihar's festive serve.<br><br>
 
            <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

            <ul type="#">

                <li>-> 1 cup Whole Wheat Flour</li><br>
                <li>-> 1/2 cup Chana dal (Bengal Gram Dal)</li><br>
                <li>-> 1 teaspoon Cumin (Jeera) seeds</li><br> 
                <li>-> 1 teaspoon Red chilli powder</li><br> 
                <li>-> 1 teaspoon Coriander (Dhania) Powder</li><br> 
                <li>-> 1/4 teaspoon Turmeric powder (Haldi)</li><br> 
                <li>-> 1 tablespoon Coriander (Dhania) Leaves,chopped finely (optional)</li><br>
                <li>-> Salt  , to taste</li><br>
                <li>-> Cooking oil , to deep fry</li></ul><br><br>


 
            <font size="7" color="#bf55ec"><u>Instructions</u></font><br>
            <ul type="#">

                <li> To begin making Bihari Chana Dal Puri Recipe, pressure cook channa dal with enough water <br>
                for about 2 whistles or until cooked enough to get easily mashed. <br>
                Also do not overcook the dal since it will make it mushy. Once chana dal is cool, drain all the<br>
                water and mash it. It is up to you to mash it completely or leave it grainy.</li><br>

                <li> Heat oil in a wide bottomed pan.<br>
                Add cumin seeds and let it splutter. Then add mashed channa dal and the spice powders and salt.<br>
                Mix well. Cook for about 2 to 3 minutes. You can add a little coriander leaves too at this stage.<br>
                Remove the dal from the pan and transfer it to a bowl to cool.</li><br>

                <li> Once it cools down, make lemon sized balls and keep aside.</li><br>

                <li> Knead whole wheat flour with little salt and water into stiff but soft dough. Cover to let it sit for about 30 minutes.</li><br>

                <li> Heat oil in a wide pan for deep frying.</li><br>

                <li> Divide the dough into equal sized balls. Flatten one of the ball into 3 to 4 inch diameter circle.<br>
                Place a ball of spicy chana dal stuffing in the middle. Carefully bring all the edges of the dough to seal the stuffing well.</li><br>

                <li> Flatten this into small circles making sure the stuffing does not fall apart.<br>
                Carefully transfer the flattened puri into hot oil. Flip to let the puri puff on both sides. <br>
                These stuffed puris will not give a huge puff like unstuffed plain ones, though. Once puris appear brownish and done,<br>
                transfer them on paper towels to drain excess oil. Repeat the step for all the remaining dough to get puris.</li></ul><br><br>   

   <font size="7" color="#bf55ec"><u>Assemble and Serve:</u></font><br>

        Serve this Bihari Chana Dal Puri Recipe hot with any side dish of your choice, like Soya Kheema,<br>
        Kaale chane ki ghugni, Mooga Gathi or Methi Paneer Bhurji in cream gravy.<br>
        It can also be served with kheer, traditionally that's how it is served. When served with choice of sabzi and steaming hot rice and dal,<br>
        it makes a filling dinner idea.<br>

  
