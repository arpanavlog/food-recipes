         <font size="7"><u><b>1) Masala Dosa-</b></u></font><br>
             Rice is a staple of south Indian cuisine owing to its usage in most of the dishes, including the finger-licking masala dosa. While dosa is a generic south Indian dish, masala dosa has specific origins in coastal Karnataka. The preparation of the rice crepe is relatively simple rice and lentils are soaked in water for five to six hours, to prepare the batter, and then it is cooked on a skillet. The types of filling in masala dosa varies, but is usually a potato and onion curry, dipped in chutney.<br>
         <center><img src="../top 10 image/13040792475_51fdc1f447_k.jpg"width="50%"/></center><br>
            <br>
          
          <font size="7"><u><b>2) Rogan Josh-</b></u></font><br>
             This is an aromatic lamb curry hailing from Kashmir, although it has roots tracing back to Persian cuisine. Composing of an array of spices, tomatoes, ginger and garlic, Rogan Josh is a mouthwatering dish adored all over the country. Although the recipe can vary from place to place, the underlying essence remains the same.<br>
         <center><img src="../top 10 image/1024px-rogan_josh.jpg"width="50%"/></center><br>
            <br>

          <font size="7"><u><b>3) Hyderabadi Biriyani-</b></u></font><br>
             Biriyani entered the foray of Indian gastronomy with the incoming Mughals, and it hasn't left with their departure. Among the numerous biriyani styles, hyderabadi biriyani stands out due to the mode of cooking and ingredients - rice, meat (lamb or chicken), yogurt, onions and significant amount of spices to give it a full-on punchy flavour.<br>
         <center><img src="../top 10 image/biryani.jpg"width="50%"/></center><br>
            <br>

        <font size="7"><u><b>4) Indian Chaats-</b></u></font><br>
             Chaats (savoury snacks) are are the mainstay of Indian street food. Popular snacks include kachori, pani puri, bhel puri and masala puri, with a base of puffed rice and peas, vegetables and spices. Chaats originated in north India, and gradually spread to every nook and corner of the country. It's no exaggeration to say that once you've tried chaats, you'll keep going back for more!<br>
         <center><img src="../top 10 image/6456856581_b68cf171fd_b.jpg"width="50%"/></center><br>
            <br>

        <font size="7"><u><b>5) Makki ki Roti and Sarson ka Saag-</b></u></font><br>
             Corn flour bread (Makki ki Roti) and a mustard leaf-based dish (Sarson ka Saag) together are a staple food of the Punjab region in India. It may not look particularly enticing, but the flavour is rich and satisfying. The nutritive value of Makki ki Roti aur Sarson ka Saag has a hand in all the prominence the dish enjoys.<br>
         <center><img src="../top 10 image/1024px-makki_di_roti_te_sarson_da_saag.jpg"width="50%"/></center><br>
            <br>

        <font size="7"><u><b>6) Tunde ke Kabab-</b></u></font><br>
           This Lucknow-based minced meat kebab is renowned for its ingredients comprising of around 150 different spices. Conventional tunde kebabs are made from buffalo meat, with variations on the recipe using chicken or mutton. Tunde ka Kabab is chief among the dishes of the Awadhi cuisine known for its ambrosial delicacies.<br>
      <center><img src="../top 10 image/Veg__Tunday_Kababi.jpg"width="50%"/></center><br>
            <br>  

         <font size="7"><u><b>7) Vada Pav-</b></u></font><br>
            Streets in Maharashtra are incomplete without the presence of vada pav sellers, such is its sway over the local gastronomy. Vada pav is a vegetarian fusion of potato patty, chilli and other spices sandwiched in a bread roll known as pav. What began as a simple and cost-effective snack has gone on to garner widespread popularity in India.<br>
      <center><img src="../top 10 image/3437240332_48cea3c8ca_o.jpg"width="50%"/></center><br>
            <br>

        <font size="7"><u><b>8) Beef roast and Parotta-</b></u></font><br>
            The people of Kerala in India are connoisseurs of beef cookery. The presence of beef in Kerala's cuisine has inspired some of the best dishes that always tempt your taste buds and beef roast is chief among them. Savouring the beef roast with Kerala's very own bread, parotta, is every meat-eater's delight.<br>
      <center><img src="../top 10 image/traditional_beef_ularthiyathu.jpg"width="50%"/></center><br>
            <br>

        <font size="7"><u><b>9) Dhokla-</b></u></font><br>
           Vegetarian savoury snacks in India do not come any better than dhoklas, made from rice and chickpeas. The fermented rice batter is mixed with chickpeas and steamed, and is then served garnished with mustard seeds and coriander. Among the plethora of vegetarian dishes from Gujarat, dhokla is arguably the forerunner.<br>
      <center><img src="../top 10 image/white_dhokla.jpg"width="50%"/></center><br>
            <br>

        <font size="7"><u><b>10) Smoked Pork-</b></u></font><br>
            Northeast Indian cuisine has its distinctive features from the rest of the country, making it an altogether unique culinary adventure. Smoked pork from Nagaland, for instance, incorporates intriguing elements, such as bamboo plants and raja mirchi - one of the hottest chillies in the world. Smoked pork is a traditional staple food of Nagaland, usually consumed with rice.<br>
