    <font size="7"><u><b>1) Rajbhog:</b></u></font>
<center><img src="../images/rajbhog-1.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Rajbhog is soft and moist with a rich flavour, somewhat similar to rasgulla,
 but stuffed with dry fruits. Though store-bought paneer or chenna too
 can be used to make rajbhog, for that unique taste and texture, split
 the milk using lemon juice and extract fresh chenna at home..<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Fresh paneer (chenna) (200 gms or 1 cup)</li><br>
<li>Cornflour (makai ka atta) (1 tbsp)</li><br></ul>

<font size="7" color="black"><b>For The Sugar Syrup:</b></font></u><br><br>
<ul type="disc">
<li>Sugar (1 cup)</li><br>
<li>Water (1/2 cup)</li></ul><br>

<font size="7" color="black"><b>For Flavouring:</b></font></u><br><br>
<ul type="disc">

<li>Saffron strands (kesar) (4-5)<br></li></ul><br>


<font size="7" color="#bf55ec"><u>Method:</u></font></u><br><br>
<ul type="disc">

<li>Crumble paneer, add maida and knead it for 5â€“10 mins to get soft 
dough. Divide it into smaller portions.</li><br>

<li>Soak pistachios and almonds in warm water for 10 mins and then 
peel the skin.</li><br>
<li>Coarsely grind them for stuffing. Take one small portion of the 
divided dough, shape it like a cup and fill it with 1/4 tsp of dry
 fruits. Now roll into a smooth peda (ball). Repeat this process for
 the remaining dough.</li><br>
 
<li>Heat sugar and water in a kadhai. When the sugar completely melts,
 add the prepared pedas, cover and allow them to cook for 20 mins on 
 medium flame, stirring occasionally to bring out that soft & moist 
 texture.</li><br>
 
<li>Note: If the sugar syrup thickens, do add a tablespoon of water 
and continue cooking.</li><br>

<li>Once the raj bhog is cooked, switch off the heat.</li><br>

<li>Add crushed saffron strands and allow the raj bhog to rest for
 6-7 hrs at least.</li><br>
 
<li>Time to serve this yummy bite of deliciousness.</li></ul><br>

