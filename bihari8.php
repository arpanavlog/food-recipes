        <font size="7"><u><b>8) Pirukia-</b></u></font>
            <center><img src="../images/Pirukia.jpg" width="40%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Very similar to Gujia, pirukia is another heavenly dessert for those with a sweet tooth. 
            Stuffed with sweetened khoya, coconut, cardamom powder and dry fruits in a crispy covering and
            dipped in sugar-syrup, pirukia is a dish from the traditional food of Bihar that is enough to sweeten your senses!
            <br><br>

       <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

            <ul type="#">

                <li>-> All-purpose flour (maida) - 1 cup.</li><br>
                <li>-> Sooji (semolina) - half cup.</li><br>
                <li>-> Powdered sugar - 1 cup.</li><br>
                <li>-> Raisin - 1/4 cup.</li><br>
                <li>-> Dry coconut (grated) - 3 tablespoons.</li><br>
                <li>-> Cardamom powder - half tea spoon.</li><br>
                <li>-> Ghee - to fry.</li><br>

            <font size="7" color="#bf55ec"><u>Instructions</u></font><br><br>
            <ul type="#">

                <li>First make a stif dough with flour, ghee and water.</li><br>
                <li>Roll out it into smaller circles. Keep it aside.</li><br>
                <li>Fry sooji in ghee and add raisins, elaichi powder and badam to it. Let it cool.</li><br>
                <li>Next, take a small quantity of the stuffing, approximately three tea spoon, in the centre of each circles.</li><br>
                <li>Fold it in half and edge it with fork.</li><br>
                <li>Deep fry it at mediumflame till it turns pink and not red.</li><br></ul>

