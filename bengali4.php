    <font size="7"><u><b>4) Tomato Chutney:</b></u></font>
<center><img src="../images/tmatar ki chatni.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
This chutney has a tangy and spicy-sweet flavour. Aside from using whole spices in the chutney to flavour it,
 make sure you fry the chopped tomatoes over the stovetop and crush them over
 low flame to give the chutney its authentic flavour..<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Tomatoes, finely chopped (6)</li><br>
<li>Sugar (2 tbsp)</li><br>
<li>Salt (to taste)</li><br></ul>

<font size="7" color="black"><b>For Tempering:</b></font></u><br><br>
<ul type="disc">
<li>Mustard (sarson) oil (1 tbsp)</li><br>
<li>Panch phoran (Bengali spice blend) (1 tsp)</li><br>
<li>Bay leaf (tej patta) (1)</li><br>
<li>MTR Haldi/Turmeric Powder (1/4 tsp)</li><br>
<li>MTR Lal Mirch/Red Chilli Powder (2 tsp)</li><br></ul>


<font size="7" color="#bf55ec"><b>Method:</b></font><br><br>
<ul type="disc">
<li>Heat oil in a kadhai over low flame and temper with the ingredients
 given under 'for tempering'.</li><br>
 
<li>Add in tomatoes, sugar, salt and mix well.</li><br>

<li>Cover and cook them over low flame till the excess moisture is absorbed and
 the oil separates from the sides of the pan.</li><br>
 
<li>The Bong-style tomato chutney is ready to be served.</li><br></ul>

