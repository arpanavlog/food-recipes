    <font size="7"><u><b>1) Kothimbir Vadi:</b></u></font>
<center><img src="../images/Kothimbir-Vadi.jpg"width="35%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Each family has its own recipe for making this dish. For a healthier option, 
reduce the amount of gram flour and add atta or jowar flour instead. <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Coriander leaves (hara dhania), washed and finely chopped (2 cups)</li><br>
<li>Gram flour (besan) (1 cup)</li><br>
<li>Water (1 cup)</li><br>
<li>MTR Lal Mirch/Red Chilli Powder (1 tbsp)</li><br>
<li>MTR Garam Masala (1/2 tbsp)</li><br>
<li>MTR Dhaniya/Coriander Powder (1/2 tbsp)</li><br>
<li>MTR Turmeric/Haldi Powder (1/2 tsp)</li><br>
<li>Asafoetida (hing) (1/4 tsp)</li><br>
<li>Ginger–garlic paste (adrakh-lasun) (1 tbsp)</li><br>
<li>White sesame seed (safed til) (1 tbsp)</li><br>
<li>Oil (for deep frying)</li><br>
<li>Salt ( to taste)</li><br>
<li>Rice flour (chawal ka atta) (1/4 cup)</li><br>
<li>Groundnut (moongfali) powder (1/4 cup)</li></ul><br>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Heat 2 tbsp oil in a pan, add asafoetida, ginger–garlic paste and cook 
for a minute.</li><br>

<li>Add sesame seeds, MTR Lal Mirch/Red Chilli Powder, MTR Garam Masala, 
MTR Dhaniya/Coriander Powder and MTR Turmeric/Haldi Powder.</li><br>

<li>Stir in the coriander leaves and mix for a few minutes.</li><br>

<li>Add groundnut powder, gram flour, rice flour and salt, stir well.</li><br>

<li>Add water and cook on medium flame until the flour absorbs the water. 
Stir consistently until a dough ball forms in the pan.</li><br>

<li>Grease a deep dish or pan.</li><br>

<li>Turn off the flame and remove the dough into the greased pan. When cool
 enough to touch, flatten the dough to make a half-inch thick pancake. 
 Set this aside for 15 minutes.</li><br>
 
<li>Remove the cooled dough onto an oiled surface and cut into 2-inch cubes.</li><br>

<li>Deep fry the cubed pieces until reddish brown.</li><br>

<li>Serve warm with your favourite chutney and chai.</li></ul><br>
  
