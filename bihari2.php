		    <font size="7"><u><b>2) Parwal-Mithai-</b></u></font>
            <center><img src="../images/Parwal-Mithai.jpg" width="50%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Biharis sure seem to have a sweet tooth. Yes you read that right, <br>
            they even turned the plain old vegetable like parwal/patol (pointed gourd) into a sweet dish, reverberant  of the food of Bihar.
            And boy, it tastes heavenlly.<br> The inner pulp of the parwal is scooped out, bolied, dipped in syrup and filled with sweet khoya preperation. 
            The end result looks as royal as it tastes...<br><br>
 
            <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

            <ul type="disc">

                <li>-> Parwal - 15 (500 grams)</li><br>
                <li>-> Sugar - 1.5 cup (300 grams)</li><br>
                <li>-> Mawa - 1 cup (200 grams)</li><br>
                <li>-> Powdered sugar - 1/4 cup</li><br>
                <li>-> Almonds - 1/4 cup</li><br>
                <li>-> Green cardamom - 5</li><br>
                <li>-> Pistachios - 1 tsp</li><br></ul>

 
            <font size="7" color="#bf55ec"><u>Instructions</u></font><br>
            <ul type="1">

                <li>i) Wash the parwal thoroughly with water and allow them to dry out completely. Then discard the stalk from both the<br>&nbsp;&nbsp; ends of the 
	            parwal and peel off their skin. Cut each parwal lengthwise in such a way that the other end remains joined<br>&nbsp;&nbsp; and scrape out the 
	            pulp and seeds, keep them in a bowl. Finely crumble the mawa and keep it in a bowl. You can grate it<br>&nbsp;&nbsp; manually using a grater.</li><br>
	
                <li>ii) Thinly slice 10 almonds and the pistachios and keep them separately. Grind the remaining almonds into fine powder in<br>&nbsp;&nbsp;&nbsp; mixer grinder,
	            transfer it in another bowl. Place a vessel on flame and pour 4 cups of water in it. Cover the vessel so<br>&nbsp;&nbsp;&nbsp; that the water starts boiling 
	            quickly. </li><br>
				
				<li>iii) Once the water starts boiling, drop the parwal in it then cover and cook them for 4 minutes.
	            minutes later, parwal will<br>&nbsp;&nbsp;&nbsp;&nbsp; get cooked aptly. Turn off the flame, leave the vessel covered as it is for 5 minutes. After 5 minutes, 
	            remove the lid <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;and transfer the parwal on a sieve placed over a bowl so that the excess water gets drained out in the bowl. Let them<br>&nbsp;&nbsp;&nbsp;&nbsp; cool down.</li><br>
	
                <li>iv) To prepare the sugar syrup, take sugar in a vessel along with 1 cup of water and place it on flame to simmer until the<br>&nbsp;&nbsp;&nbsp;&nbsp; sugar dissolves completely. 
	            Stir in-between. As the sugar gets dissolved completely, squeeze out the water from each<br>&nbsp;&nbsp;&nbsp;&nbsp; parwal and drop them in the sugar syrup. Make sure 
	            that the parwal doesn't break. Cook the parwal in sugar syrup<br>&nbsp;&nbsp;&nbsp;&nbsp; until its colour changes slightly.</li><br>
	
                <li>v) After a while, sugar syrup will become thick and the colour of the parwal will be changed as well. To check the<br>&nbsp;&nbsp;&nbsp; consistency of the sugar syrup, 
	            take few drops of it in a bowl. When it cools down, stick it between the fingers. If a<br>&nbsp;&nbsp;&nbsp; thread appears between the fingers that 
	            means sugar syrup is ready. Turn off the flame, cover and shift the vessel on<br>&nbsp;&nbsp;&nbsp; wire rack. Keep the parwal drenched in sugar syrup for 1 
	            hour so that they become sweet.</li><br>
	
                <li>vi) Meanwhile, prepare the stuffing for the parwal. For this, heat a pan and put the mawa in it. Keep stirring constantly
	            <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;and roast the mawa on low flame so that it doesn't stick to the pan. When colour of the mawa changes slightly, turn<br>&nbsp;&nbsp;&nbsp;&nbsp; off the
	            flame and shift the pan on wire rack. Allow it to cool down. As the mawa cools down, add almond powder and<br>&nbsp;&nbsp;&nbsp;&nbsp; powdered sugar in it 
	            and mix thoroughly. Transfer the stuffing in a bowl.</li><br>
	
                <li>vi) After an hour, parwal will become sweet. Carefully remove the excess sugar syrup from the parwal and keep them on a<br>&nbsp;&nbsp;&nbsp;&nbsp; plate.
	            Then stuff each parwal generously with the mawa-almond stuffing. Place 2 to 3 almonds and 2 to 3 pistachios <br>&nbsp;&nbsp;&nbsp;&nbsp;on the top and gently
	            press them. Parwal sweet is ready.</li><br>
	
                <li>vii) Serve these scrumptious and finger-licking parwal sweet as a dessert after any meal or have them whenever you<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; crave for something sweet.
	            You can store this sweet in refrigerator and relish eating for 10 days.</li></ul><br>
	
