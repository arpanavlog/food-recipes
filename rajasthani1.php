      <font size="7"><u><b>1) Daal-Baati-Churma</b></u></font>

         <center><img src="../images/dal bati churma.jpg"width="50%"/></center><br>
            <br>

      <font size="6"><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
             
      <font size="6"><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            One of the most famous dishes from the state,
            no Rajasthani food is complete without Dal Bati Churma. The round Batis dipped in ghee 
            with Panchkuti dal and churma make for an appetizing combination..<br><br>
		   
            
<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<font color="black" size="7"><b>For Baati:</b></font><br><br>

<ul type="disc">

<li>1 cup atta, also known as durum whole wheat flour</li><br>
<li>1/4 cup sooji, also known as semolina</li><br>
<li>2 tablespoons besan, also known as chickpea flour</li><br>
<li>1/4 teaspoon ajwain, also known as carom seeds</li><br>
<li>1/4 teaspoon fennel powder</li><br>
<li>1/8 teaspoon red chili powder</li><br>
<li>1/2 teaspoon salt</li><br>
<li>pinch baking soda</li><br>
<li>1/3 cup melted ghee</li><br>
<li>1/4 cup + 1 tablespoon milk, as needed to form a stuff dough</li><br>
<li>ghee, to brush on top</li></ul><br>

<font color="black" size="7"><b>For Panchmel Dal:</b></font><br><br>
<ul type="disc">

<li>1 cup mixed dal [equal amount of toor, chana, green moong, urad chilka and urad dhuli]</li><br>
<li>4 cups water</li><br>
<li>1/4 teaspoon turmeric powder</li><br>
<li>1 teaspoon salt</li></ul><br>

<font color="black" size="7"><b>For dal tempering:</b></font><br><br>
<ul type="disc">

<li>1 teaspoon vegetable oil</li><br>
<li>1 teaspoon ghee, also known as clarified butter</li><br>
<li>1/2 teaspoon mustard seeds</li><br>
<li>1/2 teaspoon cumin seeds</li><br>
<li>1.5 teaspoon finely chopped garlic</li><br>
<li>1.5 teaspoon finely chopped ginger</li><br>
<li>1 green chili, finely chopped or adjust to taste</li><br>
<li>1 medium red onion, finely chopped</li><br>
<li>2-3 medium tomatoes, finely chopped</li><br>
<li>1 teaspoon coriander powder</li><br>
<li>3/4 teaspoon cumin powder</li><br>
<li>1/4 teaspoon garam masala, more to sprinkle</li><br>
<li>salt, to taste</li><br>
<li>green cardamom powder, to sprinkle</li></ul><br>

<font size="7" color="#bf55ec"><u>Make Bati:</u></font><br><br>
<ul type="disc">

<li>Preheat oven to 375 F degrees. Line a baking tray with parchment paper and set aside.</li><br>


<li>To a bowl add atta, sooji (semolina), besan (chickpea flour), ajwain (carom seeds), fennel
 powder, red chili powder, salt and pinch of baking soda. Mix till well combined.</li><br>


<li>To this add 1/3 cup melted ghee.</li><br>


<li>Mix ghee into the flour, rubbing with your fingers till it resemble crumbs.</li><br>


<li>Now add little milk as needed to form a stiff dough and set it aside for 10-15 minutes.</li><br>


<li>After the dough has rested for a bit divide the dough into 8 equal parts. Make a ball out
 of each dough ball, press it and then make a dent in the center.</li><br>


<li>Place all the bati on a baking sheet and brush them with ghee using a pastry brush.</li><br>


<li>Bake at 375 F degrees for 15-18 minutes or till the bottom surface turns light golden
 brown in color. Then take out of the oven, flip all the bati and bake again for 15-18 
 minutes from the other side till done.</li></ul><br>


<font size="7" color="#bf55ec"><u>Make Dal:</u></font></u><br><br>
<ul type="1">
<li>To make the dal, first add all the dals in a bowl and add enough water to soak it for 
around 3-4 hours.</li><br>


<li>Once soaked, drain the water and add dal to a pressure cooker. Add 4 cups water ,
 salt and turmeric powder and mix.</li><br>


<li>Pressure cook at high for 2 whistles and then lower the heat and cook for 10-15 
minutes till dals are completely softened. Set aside.</li><br>


<li>Now heat oil and ghee in a pan on medium heat. Once hot, add mustard seeds and
 cumin seeds and let them crackle.</li><br>


<li>Then add chopped garlic, ginger and green chili and saute for few seconds or till 
they start turning golden brown.</li><br>


<li>Then add chopped onion and cook till the raw smell goes away and they turn translucent.</li><br>


<li>Now add the tomatoes for cook for 2-3 minutes.</li><br>


<li>Then add coriander powder, cumin powder, garam masala and salt to taste. 
Cook the spices for 1 minute.</li><br>


<li>Add the cooked dal to the pan now and mix. Adjust the consistency of dal at this point,
 add water to make it thinner.</li><br>


<li>Let the dal simmer for 5 minutes and then add fresh cilantro.</li><br>


<li>Sprinkle cardamom powder and garam masala powder on top and serve immediately with
 bati and extra ghee.</li><br>

 
<li>To eat crush the bati a litte in a plate, top it with lots of dal and ghee and enjoy!</li></ul>

