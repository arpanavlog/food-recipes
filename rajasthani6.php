    <font size="7"><u><b>6) Ghevar:</b></u></font>
<center><img src="../images/ghevar.jpg"width="50%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
This disc shaped sweet is prepared with  ghee,  flour,  paneer  and sugar syrup. 
It comes in various varieties like  Malai Ghevar, Mava Ghevar and  Plain  Ghevar. 
 No Rajasthani ceremony is complete without this delectable dish.<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>1 1/2 cup Maida (all-purpose flour)</li><br>
<li>1/2 cup Ghee</li><br>
<li>1/2 cup Milk</li><br>
<li>1 1/2 cup Water</li><br>
<li>Ghee – to fry</li><br></ul>

<font size="7" color="black"><b>For Sugar syrup:</b></font></u><br><br>
<ul type="disc">
<li>1 ½ cup Sugar</li><br>
<li>1 cup Water</li><br></ul>


<font size="7" color="#bf55ec"><u>Method:</u></font></u><br><br>
<ul type="disc">

<li>First take three ingredients separately i.e. milk, ghee(clarified 
butter) and water.</li><br>
 
<li>Pour ghee and cold milk in a bowl and whisk it well, so it mixes properly. 
You can use hand blender to save workload, if it’s not available, use a whisk. 
Blend it till ghee and milk emulsify into a smooth mixture and do not separate 
as shown in picture below. </li><br>
 
<li>Sieve the maida and add into the mixture above and whisk again.</li><br>
 
<li>Make sure that no lumps are left behind and the batter formed is 
smooth. Now add water little by little and mix it well into a smooth 
consistency.</li><br>

<li>Now add food color if you want (I have used oil based food color). 
Mix it well.</li><br>
 
<li>Take a round deep and flat-bottomed vessel ( I used a Pressure 
cooker as shown in the pic, Use a cylindrical vessel ).</li><br>
 
<li>Heat ghee in a deep vessel. The quantity of ghee should be half of 
the density of the vessel. Heat it on high flame till the ghee gets 
very hot. Check the right temperature by dropping a small amount of 
the batter and see if it immediately comes up, without any delay, then
 its correct temperature.</li><br> 
 
<li>Pour much batter into the ghee to make a big circle depending on 
the how thick ghevar you want to make. I used two spoonful of batter.
 Pour the batter slowly in one continuous thin stream in the centre of
 the pan.</li><br> 
 
<li>Bubbles start spluttering all over the surface of oil. Let the 
foam settle down. If the centre point is totally covered , then use 
ladle to make little space in the centre and keep on pouring the batter.</li><br>

 
<li>Once the ghevar has turned to brown in color, means it is cooked 
now. Take out the ghevar in a plate lined with tissue paper.</li><br>  
 
<li>Add water and sugar to pan. Bring them to boil with occasional 
stirring. After the boil, heat it on low flame for 5-6 minutes till 
you get a one-string consistency.</li><br> 
 
 
<li>Pour a drop of syrup on a plate to check. Once it cools down a 
little bit, take it between your thumb and a finger and see if it 
sticky or not. If you feel stickiness between your fingers forming 
a single string, then it means that your syrup is ready.</li><br>
 
<li>Now dip the ghevar into sugar syrup for 1 to 2 min and take it out.
 Keep the ghevar dipped in the syrup on a plate and tilt it, 
 so that any excess syrup gets drained.</li><br></ul>
 
