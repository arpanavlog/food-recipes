      <font size="7"><u><b>2) Boondo raita-</b></u></font>
<center><img src="../images/boondo raita.jpg"width="50%"/></center><br>
<font size="6"><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
A very famous dish enjoyed by every North Indian, it balances the taste of spices 
in your food and enhances its flavour. The mild and salty  boondo  mixed with sweet 
and sour yogurt makes a mouthwatering combination.<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>1 1/2 cups boondi</li><br>
<li>1 1/2 cups whisked chilled curd</li><br>
<li>1/2 tsp chilli powder</li><br>
<li>1 1/2 tsp chaat masala</li><br>
<li>2 tsp cumin seeds (jeera) powder</li><br>
<li>salt to taste</li><br>
<li>2 tbsp chopped coriander (dhania)</li></ul><br>

<font size="7" color="#bf55ec"><u>Method:</u></font></u><br><br>
<ul type="disc">
<li>Combine curds and approx.</li><br> 
<li>1/4 cup of water in a deep bowl and mix well.</li><br>
<li>Add the chilli powder, chaat masala, cumin seeds powder and salt and mix well.</li><br>
<li>Add the boondi and coriander and mix well.</li><br>
<li>Serve immediately.</li></ul><br>

<font size="7" color="#bf55ec"><u>Handy tips:</u></font></u><br><br>
<ul type="disc">
<li>For soft boondi, refrigerate the raita for 15 to 20 minutes and then serve.</li><br>
<li>Alternatively, you can soak the boondi in enough water for 15 minutes, drain it 
and squeeze out the excess water and add it to the curds.</li></ul>
 
