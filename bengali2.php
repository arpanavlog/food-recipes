    <font size="7"><u><b>2) Potoler Dalna:</b></u></font>
<center><img src="../images/Doi-Potol.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Potoler dalna has a thick texture with mild spice and crunchy taste to it.
You can get the taste right by sauteing pointed gourd and tempering the
 dish with panch phoron in mustard oil for that true flavour of dalna..<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Pointed gourd (potol/parwal) (10)</li><br>
<li>MTR Haldi/Turmeric Powder (1/2 tsp)</li><br>
<li>MTR Lal Mirch /Red Chilli Powder (2 tsp)</li><br>
<li>MTR Jeera/Cumin Powder (1 tsp)</li><br>
<li>Sugar (1/2 tsp)</li><br>
<li>Salt ( to taste)</li><br></ul>

<font size="7" color="black"><b>For The Sugar Syrup:</b></font></u><br><br>
<ul type="disc">
<li>Sugar (1 cup)</li><br>
<li>Water (1/2 cup)</li></ul><br>

<font size="7" color="black"><b>For Tempering:</b></font></u><br><br>
<ul type="disc">

<li>Mustard oil (sarson ka tel) (1 tbsp)</li><br>
<li>Panch phoran (1 tsp)</li><br>
<li>Clove (laung) (2)</li><br>
<li>Cinnamon (dalchini) (1/2 stick)</li><br>
<li>Bay leaf (tejpatta) (1)</li><br>
<li>Cardamom (elaichi) (1)</li></ul><br>

<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Peel and chop pointed gourd into 2-inch cylinders or cubes.</li><br>

<li>Heat oil in a pan and temper with panch phoron and whole spices.</li><br>

<li>Add chopped pointed gourd (potol), MTR Haldi/Turmeric Powder, MTR Lal 
mirch/Red Chilli Powder, MTR Jeera/Cumin Powder, salt and saute for a
 minute or two.</li><br>
 
<li>Add 1 cup of water and cook covered over low flame till the potol is 
soft and fully cooked, approximately 20 mins for thick gourds.</li><br>

<li>Add sugar, mix and then switch off the flame.</li><br>

<li>Note: Adjust the water quantity as it will change depending upon the size 
the potol cut. Also, adjust the spice and sugar quantities according to taste.
<br></li></ul><br>

