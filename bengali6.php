    <font size="7"><u><b>6) Dry Fruits Sandesh:</b></u></font>
<center><img src="../images/dry fruits.jpg"width="30%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Dry Fruits Sandesh is a Bengali dessert recipe that can be enjoyed by everyone.
 This sweet dish is prepared using nuts, full cream milk and lemon juice.
 You can serve this recipe at kitty parties, game nights, birthdays, potlucks, 
 anniversaries or any other such occasions. This Raksha Bandhan, impress your
 family and friends by making this recipe. You can also prepare this recipe 
 to serve to your guests during any other festival. This delectable recipe 
 can be prepared easily at home by following the simple steps mentioned here. 
 So, why go and buy sweets from the shops when you can make it at home in a 
 more hygienic way! Enjoy and thank us later!<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>2 litre full cream milk</li><br>
<li>1/4 teaspoon spice cardamom</li><br>
<li>1 1/2 tablespoon pistachios</li><br>
<li>3 tablespoon lemon juice</li><br>
<li>1 1/2 tablespoon almonds</li><br>
<li>150 ml maple syrup</li><br></ul>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Boil the full cream milk in a thick bottomed pan over medium flame and 
stir occasionally. Once it is boiled, turn the heat to low setting and add 
lemon juice into it. Mix well until the milk curdles.</li><br>

<li>Drain off the curdled milk using a piece of cotton cloth. (Optional: You can
 wash it under cold water again and squeeze out the excess water.)</li><br>
 

<li>Keep the curdled milk in the cloth piece for about 30 minutes and then mash it.
 Add maple syrup to it and mix well in a mixing bowl.</li><br>
 

<li>Place a kadhai over medium flame and cook the prepared mixture in it for 5
 minutes and add cardamom powder and mix well.</li><br>
 

<li>Let it cool down and then cut out squares from the dough and garnish with 
almonds and pistachios. Serve warm or cold, that's on you!.</li><br></ul>

