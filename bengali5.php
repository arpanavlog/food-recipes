    <font size="7"><u><b>5) Peyaji:</b></u></font>
<center><img src="../images/peyaji thumb.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Use only juicy red onions that have a strong flavour to make an authentic peyaji. 
Just soften the onions with salt to release the juices and you won't have to 
add extra water to make the batter.<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Onion (1 cup sliced)</li><br>
<li>Green chillies (hari mirch), finely chopped (2 tsp)</li><br>
<li>Nigella seeds (kalonji) (1/4 tsp)</li><br>
<li>Chickpea flour (besan) (1/2 cup)</li><br>
<li>Rice flour (chawal ka atta) (2 tbsp)</li><br>
<li>MTR Haldi/Turmeric Powder (1/4 tsp)</li><br>
<li>MTR Lal Mirch/Red Chilli Powder (1/4 tsp)</li><br>
<li>Water (1/4 cup)</li><br>
<li>Salt ( to taste)</li><br>
<li>Oil (for deep-frying)</li><br></ul>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>In a large bowl combine onion, salt and green chillies. Lightly crush the
 onions to release its juices. Add chickpea flour, rice flour, 
 MTR Haldi/Turmeric Powder, MTR Lal Mirch/Red Chilli Powder and nigella seeds
 and mix to form a thick paste.</li><br>
 
<li>Sprinkle water if needed and set aside for 15 mins.</li><br>

<li>Heat oil in a kadhai to deep-fry the peyaji.</li><br>

<li>Make lime-size pedas (balls) of the paste and flatten slightly.</li><br>

<li>Gently slide in the peyaji and fry until golden brown on both sides.</li><br>

<li>Peyaji tastes best served hot with kashundi and garam chai!</li><br></ul>

