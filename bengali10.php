    <font size="7"><u><b>10) Chicken and Potato Curry:</b></u></font>
<center><img src="../images/chicken and potato curry.jpg"width="30%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Bengali Chicken and Potato Curry is an authentic dish of the Bengali cuisine that 
can be prepared easily at home. <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>4 chopped into cubes potato</li><br>
<li>4 diced onion</li><br>
<li>3 tablespoon virgin olive oil</li><br>
<li>2 teaspoon garam masala powder</li><br>
<li>2 teaspoon curry powder</li><br>
<li>2 tablespoon ginger paste</li><br>
<li>4 diced tomato</li><br>
<li>1 kilograms chicken</li><br>
<li>2 teaspoon turmeric</li><br>
<li>2 teaspoon cayenne pepper</li><br>
<li>2 tablespoon garlic paste</li><br>
<li>2 teaspoon cumin</li></ul><br>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>To prepare this chicken recipe, take a non-stick pan and
 heat olive oil over medium flame. When the oil is hot enough, 
 add ginger-garlic paste in it and saute for a few seconds till the raw 
 smell goes away.</li><br>

<li>Now, add onions and saute until they becomes translucent. Add tomatoes 
and stir them well for 2 minutes.</li><br>

<li>Mix cayenne pepper, garam masala, curry powder, cumin and turmeric, salt 
and cook all the spices for 3 minutes.</li><br>

<li>Add chopped potatoes and chicken to the pan and stir to mix the all the 
ingredients completely. Cover the pan and cook for 25 minutes.</li><br>

Finally, turn off the flame and garnish the authentic chicken curry with 
mint leaves. Serve hot</li></ul><br>
  
