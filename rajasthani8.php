    <font size="7"><u><b>8) Mirch Ki Sabji:</b></u></font>
<center><img src="../images/mirch ki sabji.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Do your family members love spicy food? Then, here comes Mirch Ki Sabji
 recipe with its all new and exciting flavours. This Rajasthani recipe 
 is quite easy to prepare at home using simple ingredients like green 
 chillies, gram flour, peanuts, dry mango powder, garam masala, 
 turmeric and salt that can be an absolute delight for your taste buds.
 This simple and delectable recipe can be served on the occasions like
 kitty party, potluck, buffet. It tastes best when served warm along 
 with stuffed naan, roti or any other bread recipe. So,grab all the 
 ingredients and try out this Mirch Ki Sabji recipe at home and enjoy 
 with your family and friends!<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>8 large diced green chilli</li><br>
<li>1 cup gram flour (besan)</li><br>
<li>1 teaspoon asafoetida</li><br>
<li>1 tablespoon coconut</li><br>
<li>1 teaspoon turmeric</li><br>
<li>4 tablespoon vegetable oil</li><br>
<li>1/2 handful chopped coriander leaves</li><br>
<li>1 cup powdered raw peanuts</li><br>
<li>salt as required</li><br>
<li>1/2 teaspoon garam masala powder</li><br>
<li>1 1/2 tablespoon dry mango powder</li><br>
<li>1 1/2 gm cumin</li><br></ul>


<font size="7" color="#bf55ec"><u>How to make Mirch Ki Sabji:</u></font></u><br><br>
<ul type="disc">

<li>To begin with this recipe, wash the green chillies and chop them. 
Next, take a pan and start heating the oil over low flame.</li><br>
 
<li>Add cumin, hing, 1 teaspoon of turmeric and saute it well.
 Then, add chopped chilli pieces and saute it well for another few
 minutes.</li><br>
 
<li>Add besan to the mixture. Roast it well until it becomes golden. 
Then, add salt, coconut, peanut powder, garam masala, dry mango powder.
 saute it for 3-4 minutes or until the spices get cooked properly.</li><br>
 
<li>Finally, add the required amount of water to the mixture. Simmer it until 
the sabji get cooked completely.</li><br>

<li>Once the sabji gets cooked, turn off the flame. Garnish it with chopped 
coriander leaves and serve Mirch Ki Sabji warm with love.</li></ul><br>

