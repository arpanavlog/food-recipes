        <font size="7"><u><b>10) Laung-Latika-</b></u></font>
            <center><img src="../images/long-latika.jpg" width="40%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Laung-latika is a traditional dessert of the food of Bihar, prepared during festivals. Although similar to other stuffed delicacies, Laung latika's Clove in the centre of the crisp crust makes it stand apart from the others. The pungent taste of the clove along with sweet stuffing dipped in sugar-syrup melts in the mouth; the complex amalgam of tastes altogether makes Laung Lata exclusive to Bihar. This delicacy is perfect for cold winters with famous shops located near the Patna Meusum.<br><br>
 
       <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br> 

            <font size="7" color="Black"><b>For The Dough:</b></font><br><br> 

            <ul type="#"> 

              <li>-> 3/4 cup plain flour (maida)</li><br>
              <li>-> 1 tbsp ghee</li><br>
              <li>-> salt to taste</li></ul><br>

            <font size="7" color="black"><b>To Be Mixed Into A Filling:</b></font><br><br>
            <ul type="#">

                <li>-> 3/4 cup crumbled mawa (khoya)</li><br>
                <li>-> 1/2 cup chopped nuts (almonds , pistachios and cashewnuts)</li><br>
                
                <li>-> 1/2 tsp warm milk</li><br>
                <li>-> 1/4 tsp saffron (kesar) strands (kesar)</li><br>
                <li>-> 1/4 tsp powdered sugar</li><br>
                <li>-> 1/2 tsp cardamom (elaichi) powder</li></ul><br>


          <font size="7" color="black"><b> For The Sugar Syrup:</b></font><br><br>
               <ul type="#">

                 <li>-> 1 cup sugar</li></ul><br>

          <font size="7" color="black"><b> Other Ingredients:</b></font><br><br>
               <ul type="#">

                <li>-> 12 cloves (laung / lavang)</li><br>
                <li>-> ghee for deep-frying</li></ul><br>

          <font size="7" color="black"><b> For The Garnish:</b></font><br><br>
               <ul type="#">

                <li>-> 4 to 5 saffron (kesar) strands</li></ul><br><br>

       <font size="7" color="#bf55ec"><u> Method:</u></font><br><br>

        <font size="7" color="black"><b> For The sugar syrup:</b></font><br><br>
               
               <ul type="#">

                <li>Combine the sugar and 1,1/2 cups of water in a deep non-stick pan, mix well and cook on a medium flame for 20 minutes, while stirring occasionally. Keep aside.</li></ul><br><br>

        <font size="7" color="black"><b> For The sugar syrup:</b></font><br><br>
               
              <ul type="#">
                <li>Divide the dough into 12 equal portions and flatten each portion between your palms and keep aside.</li><br>
                <li>Divide the stuffing into 12 equal portions and keep aside.</li><br>
                <li>Roll out one portion of the dough in to 125 mm. (5) diameter circle.</li><br>
                <li>Put a portion of the filling into the center and fold two sides overlapping each other.</li><br>
                <li>Overlap the remaining two sides to form a book fold and seal it using a little water and a clove.</li><br>
                <li>Repeat steps 3 to 5 to make 11 more lavang latika's.</li><br>
                <li>Heat the ghee in a deep non-stick pan on a medium flame, deep-fry a few lavang latikas on a slow flame till they turn golden brown in colour from all the sides. Drain on an absorbent paper.</li><br>
                <li>Just before serving, re-heat the sugar syrup on a slow flame for a few seconds.</li><br>
                <li>Put the deep-fried lavang latika's in it, mix gently and allow it to soak for 10 to 15 minutes. Drain them lightly.</li><br>
                <li>Serve warm garnished with saffron strands.</li><br>
  
