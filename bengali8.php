    <font size="7"><u><b>8) Pista Sandesh:</b></u></font>
<center><img src="../images/pista sandesh.jpg"width="35%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Bengalis are known for their sweet language and sweet desserts. 
One of the most popular Bengali sweet recipe is "Pista Sandesh". <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>2 litre milk</li><br>
<li>200 gm pistachios</li><br>
<li>1/2 teaspoon citric acid</li><br>
<li>1 cup sugar</li><br>
<li>20 pistachios</li><br></ul>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>To start with, take a deep bottomed pan and keep it on medium flame.
 Add milk in the pan and boil it. Meanwhile, in a bowl, mix together citric 
 acid and water. When the milk gets boiled, slowly add this citric acid 
 mixture to the milk and let the milk curdle. Wait for the paneer to be 
 formed.</li><br>

<li>Once the paneer is formed, strain it out and let it cool. Don't forget 
to drain out the excess water from the paneer. Dry grind the pistachios till
they turn into a fine powder. Keep them aside.</li><br>
 

<li>Take a pot and roast the paneer that you just made along with sugar for
 6-7 minutes. Keep the flame low. In a grinder, grind this mixture and let
 it become tender. Add powdered pistachios to this mixture and mix well. 
 Make small balls of these mixture and garnish with pistachios if you like.</li></ul><br>
  
