    <font size="7"><u><b>10) Pista Kalakand:</b></u></font>
<center><img src="../images/pista kalakand.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Craving for a sweet milky treat? Then Kalakand is the thing to go for. 
Prepared with the goodness of milk and pista, this delicacy makes for a
 perfect after meal delight. Kalakand is a popular dessert recipe, 
 which you can find on several festivals and occasions. Moreover, this
 delicacy is pretty easy to prepare at home without putting in much 
 efforts. Pista Kalakand is a flavourful sweet dish recipe that sets 
 the festive mood to a new high. The process to prepare this kalakand 
 recipe is time consuming, but you'll definitely love the taste when 
 its made.<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>100 gm pistachios</li><br>
<li>1 teaspoon ghee</li><br>
<li>1 teaspoon powdered green cardamom</li><br></ul>

<font size="7" color="black"><b>For The Main Dish:</b></font></u><br><br>
<ul type="disc">
<li>5 litre milk</li><br>
<li>350 kilograms sugar</li></ul><br>

<font size="7" color="#bf55ec"><u>How to make Pista Kalakand:</u></font></u><br><br>
<ul type="disc">

<li>To make this amazingly delicious sweet recipe, you just need a few 
ingredients in place and you are good to go. Take a thick-bottomed
 pan and heat it over medium flame, add milk and allow it to boil.</li><br>
 
<li>Keep stirring the milk and allow the blend to turn slightly thick in consistency.
 Once the blend starts leaving the sides of the pan, turn off the flame
 and move to the next step.</li><br>
 
<li>Put the pistachios (leave a few for garnishing) in a blender and make 
a fine powder. Then add this pistachios powder along with sugar and 
green cardamom to the milk blend. Stir it for a few minutes and remove 
from the flame. Grease a large plate with ghee and spread this mixture 
on it.</li><br>
 
<li>prinkle green cardamom powder and garnish with crushed pistachio flakes, 
put the plate in the refrigerator and let the mixture settle down for 
about an hour. Once the mixture has a nice consistency, cut them into 
small pieces and serve cold.</li></ul><br>

