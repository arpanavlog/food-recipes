        <font size="7"><u><b>5) Makhane-ki-kheer-</b></u></font>
            <center><img src="../images/Makhane-ki-kheer.jpg" width="40%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Makhane Ki Kheer is a famous pudding, originating from Bihar. 
            Its popularity is widespread in India.This sweet, creamy dessert is considered to
            be an auspicious food in Bihar households. What makes this dish a bit different from
            the usual kheer is that it is prepared with foxnuts. Foxnuts are roasted and lightly ground before use..
            <br><br>
 
            <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

            <ul type="#">

            <li>-> Foxnut (makhana)- 1 cup</li><br>
            <li>-> Clarified butter (ghee)- 1 table spoon</li><br>
            <li>-> Milk (doodh)- 1 litre</li><br>
            <li>-> Condensed Milk - 1/2 can</li><br>
            <li>-> Sugar (chini)- 4-5 table spoons</li><br>
            <li>-> Cardamom (elaichi) powder - 1/4 tea spoon</li><br>
            <li>-> Cashew nuts (kaju)- 1/4 cup</li><br>
            <li>-> Raisins (kishmis)- 1/4 cup</li><br>
            <li>-> Saffron - Few strands</li></ul><br>

            <font size="7" color="#bf55ec"><u>Instructions</u></font><br>
            <ul type="#">

                <li> Heat clarified butter in a pan and roast makhana in ghee until crisp and aromatic.</li><br>
  
                <li> Take half of the roasted Makhana in a blender jar and grind it coarsely and keep aside.</li><br>
  
                <li> Now take half of the raisins and cashew nuts in a blender jar and also coarsely grind them.</li><br>
  
                <li> Mix milk, condensed milk and coriander powder in a heavy bottom pan and boil until it becomes almost half.
                Keep stirring in between to prevent burning.</li><br>
  
                <li> Now add sugar and let it boil for 5-7 minutes.</li><br>
  
                <li> Then add the whole and coarsely ground Makhana and dry fruits.   
                Mix well and cook on a medium flame for 5 to 10 minutes until it becomes thick.</li><br>
     
                <li> Add the saffron and put the flame off. Serve this hot or chilled.</li></ul><br>
  
