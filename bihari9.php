        <font size="7"><u><b>9) Kadhi-Badi-</b></u></font>
            <center><img src="../images/Kadhi-Badi.jpg" width="50%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            This all-time favourite yummilicious (if such a word exists) dish is another
            staple delicacy of Bihari food. The gravy (Kadhi) as well as the badi are prepared 
            from besan (gram flour). The gravy also consists of yoghurt. It is usually complimented 
            with rice and/or puris.<br><br>
 
        <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

          <font size="7" color="black"><b>For the kadhi:</b></font><br><br>
             <ul type="#">
              <li>-> 1 cup sour curd</li><br>
              <li>-> 1 tbsp besan</li><br>
              <li>-> 1 tbsp  ginger-garlic paste</li><br>
              <li>-> 1 tsp black pepper powder</li><br>
              <li>-> 1 tsp cumin powder</li><br>
              <li>-> 1 tsp coriander powder</li><br>
              <li>-> 1 tsp turmeric powder</li><br>
              <li>-> 1tsp red chilli powder</li><br>
              <li>-> 1-2 slit green chillies</li><br>
              <li>-> Salt to taste</li></ul><br>

          <font size="7" color="black"><b> For Badi or Bari:</b></font><br><br>

            <ul type="#">

              <li>-> 1 cup besan</li><br>
              <li>-> 1 tsp ajwain/carom seeds</li><br>
              <li>-> 1/2 tsp Kalonji/onion seeds</li><br>
              <li>-> 1 tsp haldi/turmeric  powder</li><br>
              <li>-> 1 tsp red chilli powder (optional)</li><br>
              <li>-> 1 onion chopped</li><br>
              <li>-> 1 pinch cooking soda</li><br>
              <li>-> Oil for frying</li><br>
              <li>-> Salt to taste</li></ul><br>

          <font size="7" color="black"><b> For tempering:</b></font><br><br>

            <ul type="#">

              <li>-> 1 tsp cumin seeds</li><br>
              <li>-> 1 red chilli broken into pieces</li><br>
              <li>-> A few curry leaves</li><br>
              <li>-> 1 tbsp oil</li></ul><br>

        <font size="7" color="#bf55ec"><u>Instructions</u></font><br><br>
            <ul type="#">

            <font size="7" color="black"><b>Making the Badi or Bari:</b></font><br><br>
            <ul type="#">
                
              <li>In a big bowl mix the besan, ajwain, kalonji, turmeric, red chilli powder if using, 
              onion and the cooking soda with water to make a thick batter.</li><br>
              <li>Set aside for 15 minutes.</li><br>
              <li>Heat oil in deep kadhai/wok.</li><br>
              <li>Drop small balls of the batter in the hot oil. I used my hands but you can use 2 
              teaspoons to do the job.(Add small balls as they will swell in the oil).</li><br>
              <li>On medium flame fry until deep golden brown.</li><br>
              <li>Drain and set aside on absorbent paper.</li></ul><br>

         <font size="7" color="black"><b> Making the kadhi:</b></font><br><br>
      
            <li>Mix the curd, besan ginger-garlic paste, black pepper powder, cumin powder, coriander powder,
            turmeric powder, red chilli powder and 2 cups water. Add more water if you want a thinner kadhi.</li><br>
            <li>whisk so that there are no lumps.</li><br>
            <li>In a deep pan heat oil add the jeera/cumin seeds to splutter.</li><br>
            <li>Add the red chilli, curry leaves and the green chillies.</li><br>
            <li>Add the kadhi mix stirring constantly.</li><br>
            <li>simmer on low flame for 15 minutes.</li><br>
            <li>Put the badi in the simmering kadhi.</li><br>
            <li>Cover and cook for 5 more minutes.</li><br>
            <li>Serve with rice.</li></ul><br>

