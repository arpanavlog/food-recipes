    <font size="7"><u><b>3) Batata Kacharya:</b></u></font>
<center><img src="../images/batata kacharya.jpg"width="35%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
The potato slices need to be thin enough to be cooked without water.
Stir them consistently to prevent them from burning. <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>Potatoes (aloo) (4-5)</li><br>
<li>Green chilies (hari mirch) (2-4)</li><br>
<li>Whole red chilies (sabut lal mirch), broken (1)</li><br>
<li>MTR Haldi/Turmeric Powder (1 tsp)</li><br>
<li>Mustard seeds (sarson) (1 tsp)</li><br>
<li>Asafoetida (hing) (1 tsp)</li><br>
<li>Curry leaves (kadi patta) (3-5)</li><br>
<li>Oil (for frying)</li></ul><br>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Wash and peel the potato skin. Cut the potatoes into thin slices.</li><br>

<li>Heat oil in a kadhai. Add sarson and let it splutter.</li><br>

<li>Add hing and MTR Haldi/Turmeric Powder.</li><br>

<li>Add kadi patta and red and green chilies. Saut√© the chilies.</li><br>

<li>Add chopped potatoes and mix them.</li><br>

<li>Fry the potatoes till they become tender.</li><br>

<li>Add salt and heat till the potatoes are crisp. </li><br>

<li>Garnish with hara dhania and serve hot.</li></ul><br>
  
