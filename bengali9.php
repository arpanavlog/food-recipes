    <font size="7"><u><b>9) Doi Rui:</b></u></font>
<center><img src="../images/doi rui.jpg"width="40%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Doi Rui is an easy-to-make Bengali fish curry recipe which can be an ideal pair for steamed rice.
 Prepared with Rohu fish, curd, ginger-garlic paste, green chillies and some 
 spices, this dish can be perfect for the bachelor's kitchen! Durga Puja is 
 just around the corner and Bengalis are ready for feasting! <br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>750 gm rohu fish</li><br>
<li>4 green chillies</li><br>
<li>2 cup yoghurt (curd)</li><br>
<li>2 teaspoon garlic paste</li><br>
<li>salt as required</li><br>
<li>3 tablespoon mustard oil</li><br>
<li>2 1/2 tablespoon onion paste</li><br>
<li>1 teaspoon garam masala powder</li><br>
<li>2 teaspoon ginger paste</li><br>
<li>1 tablespoon sugar</li><br>
<li>1 1/2 teaspoon turmeric</li><br>
<li>1/2 teaspoon Kashmiri red chilli</li></ul><br>


<font size="7" color="#bf55ec"><u>Method:</u></font><br><br>
<ul type="disc">
<li>Wash the fish pieces and keep them in a large glass bowl.</li><br>

<li>Add salt, turmeric, ginger paste, sugar, garlic paste, garam masala powder, 
kashmiri chilli powder, onion paste and curd in it.</li><br>
 

<li>Mix well and cover with a lid or plate. Then, let the fish marinate in the
 mixture for 20 minutes</li><br>
 
<li>Heat mustard oil in a kadhai over medium flame and add chopped green 
chillies in it.</li><br>

<li>Once the chillies turn brownish in colour, add the prepared marinade 
and fish pieces slowly in the kadhai.</li><br>

<li>Put the flame to low setting and let it cook for 30 minutes. Stir 
occasionally.</li><br>

<li>Then, turn off the flame and cover with a lid and let the flavours soak in the dish for 10 minutes. 
Serve hot with steamed rice.</li></ul><br>
  
