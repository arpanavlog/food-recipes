        <font size="7"><u><b>4) Dal-Pitha-</b></u></font>
            <center><img src="../images/Dal-Pitha.jpg" width="40%"/></center>
            <br><i><p><font size="6">
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            Bihari Dal Pitha Recipe (Traditional Bihari Delicacy) is a popular rice flour dumplings in Bihari Cuisine.
            Pithas are either stuffed with sweet or spicy filling.<br> 
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            There are different varieties of Pithas. Bihari Dal Pitha is prepared using fresh rice flour and spicy masala lentil filling. The speciality of this dish is that it is vegan,
            gluten free and steamed.<br> 
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            High in proteins, this is an anytime snack, loved by all age groups. Pithas served along with
            Hari Dhaniya ki Chutney, Tomato Chutney or Mint Chutney can be a delightful starter or a specialty dinner option.<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            This "guilt free snack" can, not only replace the "fried starters" that many people gorge during parties,
            but can also satisfy the taste buds when you pop a whole dumpling in your mouth and dig into the yummy spicy filling.<br><br>
 
            <font size="7" color="#bf55ec"><u>Ingredients:</u></font><br><br>   

            <ul type="#">

                <li>-> 1 cup Rice, soaked for 4 hours or 1/2 cup rice flour</li><br>
                <li>-> 1 teaspoon Cooking oil</li><br>  
                <li>-> 1 pinch Salt</li><br>
                <li>-> Water, to cook</li><br>
                <li>-> Ingredients for Dal Stuffing (to be ground)</li><br>
                <li>-> 3/4 cup Chana dal (Bengal Gram Dal), soaked for 3 hours</li><br>
                <li>-> 2 Green Chillies, chopped</li><br>
                <li>-> 1 inch Ginger, peeled and chopped</li><br>
                <li>-> 3 cloves Garlic</li><br> 
                <li>-> 1/2 teaspoon Cumin (Jeera) seeds</li><br>  
                <li>-> 1/4 teaspoon Whole Black Peppercorns</li><br>  
                <li>-> 1/2 teaspoon Carom seeds (Ajwain)</li><br> 
                <li>-> 1 teaspoon Cooking oil</li><br>  
                <li>-> Salt, to taste</li><br>
                <li>-> Coriander (Dhania) Leaves, finely chopped for garnish</li><br>
                <li>-> 1 teaspoon Cooking oil, for greasing.</li></ul><br>


 
            <font size="7" color="#bf55ec"><u>Instructions</u></font><br>

            <font size="7" color="black"><b>make Rice Dough:</b></font><br>

            <ul type="#">

                <li>Grind the soaked rice to a smooth paste using water (approximately 1/2 cup)</li><br>

                <li>Dilute the ground batter by adding water (approximately 1.5 - 2 cups) to get a thin batter consistency.</li><br>

                <li>Add salt and oil to the batter and mix well.</li><br>

                <li>Transfer the batter to a heavy bottom kadai or vessel and start cooking over low heat.</li><br>

                <li>Keep stirring the batter to prevent lumps and cook the rice flour batter till it turns into a soft dough. 
                It might look as if the dough is turning lumpy and sticking to the ladle, but keep stirring till the entire batter cooks. 
                This step will take at least 12 -15 minutes over low flame.</li></ul><br>

            <font size="7" color="black"><b>make the Dal Stuffing:</b></font><br>

            <ul type="#">
                <li>Drain the excess water from soaked dal and grind dal along with green chillies, ginger, garlic,
                salt, carom seeds cumin seeds and pepper corns to make a coarse paste.</li><br>

                <li>Heat a kadai/wok over low flame, with a teaspoon of oil and add the ground paste.</li><br>

                <li>Keep stirring till the dal cooks well and they get a powdered/crumbly texture. This step will approximately take 7-10 minutes.</li><br>

                <li>Switch off and add the chopped cilantro leaves. Your stuffing for Bihari Dal Pitha is now ready.</li></ul><br>

          <font size="7" color="black"><b> shape Pithas:</b></font><br>

            <ul type="#">

              <li>Grease a steamer vessel with oil. Grease your hands with oil and take lemon size portions
              of the rice dough and using your hand shape them into balls. Work when the rice dough is still warm for better result.
              Keep the rice dough covered with a damp cloth or a lid to keep it moist while you are making balls.</li ><br>

              <li>Taking one ball at a time, pinch the corners and shape the balls to make a cup and spoon the dal stuffing. 
              You can also use gujiya mould to shape the pithas. Also can shape the pithas like dimsums/wontons /kozhukattai/modak.</li><br>

              <li>Seal gently by pinching the ends using greased fingers.</li><br>

              <li>Repeat the same for the entire batch.</li><br>

              <li>Place the pithas in the greased steamer and steam them for 8-10 minutes. You can also cook the pitha by dunking
              them in boiling hot water. Cook till they rise up to the surface, drain and serve. Make sure to seal the pithas properly to avoid leakage.</li><br>

              <li>Your delicious, healthy Bihar Dal Pitha Recipe (Traditional Bihari Delicacy) is ready to be served hot along
              with your choice of chutneys or sauces to make a yummy spicy filling tea-time snack.</li></ul>
  
