    <font size="7"><u><b>7) Rajasthani Mawa Mishri :</b></u></font>
<center><img src="../images/rajasthani mawa mishri.jpg"width="50%"/></center><br>
<font size="6">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Festive season is around the corner and the demand for sweets will be 
on its peak high. To make this festive season more special, try out 
Rajasthani Mawa Mishri recipe that can be loved by many. 
This Rajasthani dessert recipe is added with lots of sugar 
along with cardamom powder, pistachios, almonds and ghee that 
can truly be a delight for several sweet food lovers across the globe.
 To make this dish more tasty, you can top it up with saffron stands 
 or chopped almonds. This dessert can be tried out in various 
 festivities or served as a delicacy in dinner parties or buffet 
 nights! The creamy and soft texture of the dessert topped with 
 crunchy almonds and pistachios is sure to make you go wow! So, 
 tep in your kitchen and try out Rajasthani Mawa Mishri recipe today
 and take everyone by a surprise!<br><br>

<font size="7" color="#bf55ec"><u>Ingredients:</u></font></u><br><br>
<ul type="disc">
<li>1/2 cup mishri)</li><br>
<li>1 1/2 teaspoon black cardamom</li><br>
<li>1 1/3 teaspoon ghee</li><br>
<li>1 1/4 cup sugar</li><br>
<li>2 1/4 litre milk</li><br></ul>

<font size="7" color="black"><b>For Garnishing:</b></font></u><br><br>
<ul type="disc">
<li>1 1/2 teaspoon almonds</li><br>
<li>1 1/2 teaspoon pistachios</li><br></ul>


<font size="7" color="#bf55ec"><u>How to make Rajasthani Mawa Mishri:</u></font></u><br><br>
<ul type="disc">

<li>To begin with this recipe, take a heavy bottomed pan and start boiling the milk in it.
 Keep stirring the pan continuously till the milk begins to thicken..</li><br>
 
<li>Next, add ghee, sugar and green cardamom powder to the pan. Mix everything well. 
Allow it to cook for another 5-10 minutes.</li><br>
 
<li>When the mix in the pan appears to be cooked, turn off the flame. Allow it to cool
 down. Add mishri, pistachios and almonds to the pan. Mix everything 
 well. Your Rajasthani Mishri is now ready to be served.</li></ul><br>
 
